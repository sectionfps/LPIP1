public class Desafio1 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        String[] nomes = {"Ana", "Bruno", "Carlos", "Daniela", "Eduardo"};

        for (int i = 0; i < nomes.length; i++) {
            System.out.println(nomes[i]);
        }
    }
}